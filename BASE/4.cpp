#include <iostream>
using namespace std;

int main ()
{
    float X;
    float Y;

    cout<<"Enter a number for X : ";
    cin>>X;

    cout<<"Enter a number for y : ";
    cin>>Y;

    cout<<"Addition of X and Y : "<<X+Y<<endl;
    cout<<"Subtration of X and Y : "<<X-Y<<endl;
    cout<<"Multiplication of X and Y : "<<X*Y<<endl;
    cout<<"Division of X and Y : "<<X/Y<<endl;

    return 0;

}